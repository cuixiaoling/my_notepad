Sublime Text 3 快捷键

一，选择类
Ctrl+D 选中相同的文本
Ctrl+L 选中整行
Ctrl+Shift+L 选中的每行后面都加一个光标
Ctrl+w 关闭当前窗口
Ctrl+tab 切换窗口

Ctrl+M 光标移到结束的位置
Ctrl+Enter 在下一行加入新行
Ctrl+Shift+Enter 在上一行加入新行
Ctrl+[ 左移
Ctrl+] 右移
Ctrl+Shift+[ 折叠代码
Ctrl+Shift+] 展开代码
Ctrl+左键 向左单位性的移动光标
Ctrl+右键 向右单位性移动光标
** 选中文本
Shift+上键 向上选中多行
Shift+下键 向下选中多行
Shift+左键 向左选中文本
Shift+右键 向右选中文本
Ctrl+Shift+左键 向左单位性的选择文本
Ctrl+Shift+右键 向右单位性的选择文本
Ctrl+Shift+上键 向上移动
Ctrl+Shift+下键 向下移动
Ctrl+A+上键 向上多行光标
Ctrl+A+下键 向下多行光标

二, 编辑类
Ctrl+Shift+D 复制光标所在整行，插入到下一行
Ctrl+K+U 转换大写。
Ctrl+K+L 转换小写。
Ctrl+K+K 从光标处开始删除代码至行尾。
Ctrl+Shift+K 删除整行。

三, 搜索类
Ctrl+F 打开底部搜索框，查找关键字。
Ctrl+G 打开搜索框，自动带：，输入数字跳转到该行代码。举个栗子：在页面代码比较长的文件中快速定位。
Ctrl+R 打开搜索框，自动带@，输入关键字，查找文件中的函数名。举个栗子：在函数较多的页面快速查找某个函数。
Ctrl+： 打开搜索框，自动带#，输入关键字，查找文件中的变量名、属性名等。
Esc 退出光标多行选择，退出搜索框，命令框等。
四,显示类

Ctrl+Tab 按文件浏览过的顺序，切换当前窗口的标签页。
Ctrl+PageDown 向左切换当前窗口的标签页。
Ctrl+PageUp 向右切换当前窗口的标签页。
Alt+Shift+1 窗口分屏，恢复默认1屏（非小键盘的数字）
Alt+Shift+2 左右分屏-2列
Alt+Shift+3 左右分屏-3列
Alt+Shift+4 左右分屏-4列
Alt+Shift+5 等分4屏
Alt+Shift+8 垂直分屏-2屏
Alt+Shift+9 垂直分屏-3屏
Ctrl+K+B 开启/关闭侧边栏。
F11 全屏模式

